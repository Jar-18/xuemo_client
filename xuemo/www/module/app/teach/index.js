angular.module('starter.controllers')

    .controller('teachCtrl', ['$scope','URL_CONFIG','$http','$timeout',function($scope,URL_CONFIG,$http,$timeout) {
        var urlStatus=URL_CONFIG.status;
        $http({
            method:'GET',
            url:URL_CONFIG.common.districts[urlStatus],
            params:{}
        }).success(function(data){
            $scope.districtsList=data;
        });
        $http({
            method:'GET',
            url:URL_CONFIG.common.courseCategories[urlStatus],
            params:{}
        }).success(function(data){
            $scope.courseCategoriesList=data;
        });
        $scope.courseForm={
            options:{
                courseSite:{
                    dicName:"courseSite",
                    selectorList:["1","2","3"]
                },
                courseType:{
                    dicName:"courseType",
                    selectorList:["1","2","3"]
                },
                courseCategories:{
                    "multiple":false,
                    "addSelectAllBtn":false,
                    "modalTitle":"课程类型"
                },
                districts:{
                    "modalTitle":"区域"
                }
            },
            dataModel:{
                districts:[{"id":100103},{"id":100104}] ,
                courseCategories:[{"id":100201}]
            }
        };
        $timeout(function(){
            $scope.courseForm.dataModel.sites=[{"id":1},{"id":2}];
            $scope.courseForm.dataModel.types={"id":1};
        },1000);
        $timeout(function(){
            console.debug($scope.courseForm.dataModel);
        },3000);
    }])
